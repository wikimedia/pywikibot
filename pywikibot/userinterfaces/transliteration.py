class Transliterator:

    """Class to transliterating text."""

    def __init__(self, encoding: str) -> None:
        """Initialize the transliteration mapping.

        :param encoding: the encoding available. Any transliterated
            character which can't be mapped, will be removed from the
            mapping.
        """
        trans = _trans.copy()
        for char, value in trans.items():
            if value == '?':
                continue
            while (value.encode(encoding, 'replace').decode(encoding) == '?'
                   and value in trans):
                value = trans[value]
            trans[char] = value
        self.trans = trans

    @deprecate_arg('next', 'succ')  # since 9.0
    def transliterate(self, char: str, default: str = '?',
                      prev: str = '-', succ: str = '-') -> str:
        """Transliterate the character.

        .. versionchanged:: 9.0
           *next* parameter was renamed to *succ*.

        :param char: The character to transliterate.
        :param default: The character used when there is no transliteration.
        :param prev: The previous character
        :param succ: The succeeding character
        :return: The transliterated character which may be an empty string
        """
        result = default
        if char in self.trans:
            result = self.trans[char]
        # Arabic
        elif char == '◌':
            result = prev
        # Japanese
        elif char == 'ッ':
            result = self.transliterate(succ)[0]
        elif char in '々仝ヽヾゝゞ〱〲〳〵〴〵':
            result = prev
        # Lao
        elif char == 'ຫ':
            result = '' if next in 'ງຍນຣລຼຼວ' else 'h'
        return result


